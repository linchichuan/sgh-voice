const langNames = { ja: "日本語", zh: "繁體中文", en: "English", vi: "Tiếng Việt", th: "ภาษาไทย" };

function setLanguage(lang) {
    if (!translations[lang]) return;
    const t = translations[lang];
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (t[key]) {
            if (el.tagName === "INPUT") el.placeholder = t[key];
            else el.innerHTML = t[key];
        }
    });
    document.documentElement.lang = lang;
    document.getElementById("currentLang").textContent = langNames[lang];
    document.querySelectorAll(".lang-option").forEach(btn => {
        btn.classList.toggle("active", btn.dataset.lang === lang);
    });
    document.getElementById("langDropdown").classList.remove("open");
    localStorage.setItem("sghvoice-lang", lang);
}

// Init language — Japanese is ALWAYS the default (primary language)
(function initLang() {
    const saved = localStorage.getItem("sghvoice-lang");
    if (saved && translations[saved]) { setLanguage(saved); return; }
    // Default to Japanese regardless of browser language
    setLanguage("ja");
})();
