let currentLang = detectInitialLang();

function detectInitialLang() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && SUPPORTED.includes(stored)) return stored;
  } catch { /* ignore */ }
  const nav = (typeof navigator !== 'undefined' && navigator.language) ? navigator.language.toLowerCase() : 'ja';
  if (nav.startsWith('zh')) return 'zh';
  if (nav.startsWith('en')) return 'en';
  return 'ja';
}

/** Get the active language code. */
export function getLang() {
  return currentLang;
}

/** Get list of supported languages. */
export function getSupportedLangs() {
  return SUPPORTED.slice();
}

/**
 * Translate a key with optional {param} interpolation.
 * Falls back across ja → en → key if missing.
 * @param {string} key
 * @param {Record<string,string|number>} [params]
 */
export function t(key, params = {}) {
  const table = STRINGS[currentLang] || STRINGS.ja;
  let str = table[key];
  if (str === undefined) str = STRINGS.en[key];
  if (str === undefined) str = key;
  return str.replace(/\{(\w+)\}/g, (_, k) => (k in params ? String(params[k]) : `{${k}}`));
}

/**
 * Switch active language. Persists to localStorage and fires `langchange`.
 * @param {'ja'|'zh'|'en'} lang
 */
export function setLang(lang) {
  if (!SUPPORTED.includes(lang)) return;
  currentLang = lang;
  try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* ignore */ }
  if (typeof document !== 'undefined') {
    document.documentElement.setAttribute('lang', lang === 'zh' ? 'zh-TW' : lang);
  }
  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

/**
 * Walk the DOM and replace text content of [data-i18n-key] nodes.
 * Safe — uses textContent, never innerHTML.
 * @param {ParentNode} [root]
 */
export function applyI18n(root = document) {
  root.querySelectorAll('[data-i18n-key]').forEach((el) => {
    const key = el.getAttribute('data-i18n-key');
    if (key) el.textContent = t(key);
  });
}
